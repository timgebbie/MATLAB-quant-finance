%   CELL2TICKLIST - cell-array to a ticker list
%   DATERANGE - compute a date range from date vectors
%   ENABLEAUTOMATIONSERVER - enable an automation server for matlab
%   EPSCLEAN - remove trailing numerical noise at significant figure level
%   FREQSAMPLERATE - convert the frequency to a resampling rate
%   HOLIDAYS - compute holidays for the JSE, LSE and NYSE
%   HPFILTER - Hodrick-Prescott filter (legacy)
%   ISMEMSTR - Fast string member check
%   ITEMTRIM - trim strings of FINTS unfriendly characters
%   NANSPY - visualise NaN's in the data
%   NANZSCORE - Z-score missing data
%   NTILE - N-tile missing data 
%   OPENSTRUCT - open and check struct (TBDL)
%   SENDGMAIL - Send a GMAIL email from matlab
%   STARTUP_DEVELOPMENT_QTTIM - Tim's start up file
%   STARTUP_DEVELOPMENT_QTRAPH - Raphs's start up file
%   STARTUP_PRODUCTION - Production start up file
%   STRUCTMATVIEW - view a structure in matlab
%   UNSORT - unsort a sorted vector using sort indices
%   WINDSORIZE - windsorize missing data matrices
%   ZEROORDERHOLD - apply zero-order hold to a matric
